
//CreditoEmprendedor
function cambiarColorEmprendedor(x){
    document.getElementById("Emprendedor").setAttribute("src","../imagenes/CreditoHipotecario.jpg");
    setInterval("cambiarColorEmprendedor(x)",1000)
}
function regresarColorEmprendedor(x){
    document.getElementById("Emprendedor").setAttribute("src","../imagenes/imgCreditoEmprendedor.jpg");
    setInterval("regresarColorEmprendedor(x)",1000)
}
